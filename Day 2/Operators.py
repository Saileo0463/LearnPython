"A symbol which will perform an operation between 2 or more variables"

"1. Arithmetic Operator"

a = 5
b = 2
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a//b)
print(a%b)
print(a**b)

"2. Relational Operators (Will returns always a bool value as True/False)"

a = 5
b = 10
print(a>b)
print(a<b)
print(a==b)
print(a!=b)
print(a>=b)
print(a<=b)

"3. Logical Operators"

"Using AND operator"
x = 5
print(x > 3 and x > 10)
print(x > 3 and x > 2)
"Using OR Operator"
print(x > 3 or x < 10)
print(x > 3 or x < 3)
"Using NOT Operator"
print(not x > 10)
